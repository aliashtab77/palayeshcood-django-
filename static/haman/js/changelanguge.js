function chLaToPe() {
    const language = document.getElementById("language");
    const form = document.getElementById("changelangugefo");
    language.value = "fa";
    form.submit();
}

function chLaToEn() {
    const language = document.getElementById("language");
    const form = document.getElementById("changelangugefo");
    language.value = "en";
    form.submit();
}